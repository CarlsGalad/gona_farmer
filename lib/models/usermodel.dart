class FarmProfile {
  final String farmName;
  final String ownersName;
  final String mobile;
  final String email;
  final String address;
  final String imagePath;
  final String state;
  final String city;

  FarmProfile({
    required this.farmName,
    required this.ownersName,
    required this.mobile,
    required this.email,
    required this.address,
    required this.imagePath,
    required this.state,
    required this.city,
  });
}
